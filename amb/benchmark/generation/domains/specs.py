def select_domain_specs(requested: tuple[str, ...] | None) -> tuple[DomainSpec, ...]:
    if requested is None:
        return DOMAIN_SPECS
    by_name = {spec.domain: spec for spec in DOMAIN_SPECS}
    missing = tuple(domain for domain in requested if domain not in by_name)
    if missing:
        raise ValueError(f"unknown domain(s): {', '.join(missing)}")
    return tuple(by_name[domain] for domain in requested)


def counterfactual_spec(spec: DomainSpec, cf_index: int) -> DomainSpec:
    return apply_counterfactual_edit(spec, cf_index)
